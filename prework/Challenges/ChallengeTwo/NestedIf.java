package Challenges.ChallengeTwo;

public class NestedIf {

    public String printNumberInWord(int num) {
        String result;
        if (num == 0){
            System.out.println("CERO" );
            result = "CERO";
            return result;
        } else if (num == 1){
            System.out.println("ONE" );
                result = "ONE";
                return result;
        }else if (num == 2){
            System.out.println("TWO" );
                result = "TWO";
                return result;
        } else if (num == 3){
            System.out.println("THREE" );
                result = "THREE";
                return result;
        } else if (num == 4){
            System.out.println("FOUR" );
                result = "FOUR";
                return result;
        } else if (num == 5){
            System.out.println("FIVE" );
                result = "FIVE";
                return result;
        } else if (num == 6){
            System.out.println("SIX" );
                result = "SIX";
                return result;
        } else if (num == 7){
            System.out.println("SEVEN" );
                result = "SEVEN";
                return result;
        } else if (num == 8){
            System.out.println("EIGHT" );
                result = "EIGHT";
                return result;
        } else if (num == 9){
            System.out.println("NINE" );
                result = "NINE";
                return result;
        }else {
            System.out.println("MORE THAN NINE" );
                result = "MORE THAN NINE";
                return result;
        }
    }
}
